namespace TouchpadPeaceFree
{
    internal static class TimeDefs
    {
        internal const int MILLISECONDS = 1;
        internal const int SECONDS = (1000 * MILLISECONDS);
        internal const int MINUTES = (60 * SECONDS);
        internal const int HOURS = (60 * MINUTES);
        internal const int DAYS = (24 * HOURS);
        internal const int WEEKS = (7 * DAYS);
    }
}
